    public function show()
    {
        // load submenu
        $this->submenu();
        $data = $this->data;

        //=========================
        //change start
        $condition_fields = array(
            array('variable'=>'type', 'field'=>$this->table.'.type', 'default_value'=>'', 'operator'=>'like_both'),
            array('variable'=>'message', 'field'=>$this->table.'.message', 'default_value'=>'', 'operator'=>'like_both'),
            array('variable'=>'status', 'field'=>$this->table.'.status', 'default_value'=>'1', 'operator'=>'where'),
        );
        
